// While Loops // 


let arr = ["milk", "bread", "cheese", "butter",];
let count = 0; // This code counts how many times this loop will repeat //
while(count < arr.length){
console.log(arr[count++]);
}

// count++ >>>> This code stops it from becoming a forever loop >>>> And prints the array in the correct order // 

// We put things inside loops so that they can be executed repeatedly //